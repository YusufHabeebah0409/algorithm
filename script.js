var area = 1/2 * diagonal1 * diagonal2
console.log(area);

// Area of a Rhombus 
var diagonal1 = 10;
var diagonal2 = 8;
var area = 1/2 * diagonal1 * diagonal2
console.log(area);

// Area of Trapezium 
var base1 = 5;
 var base2 = 7;
 var height = 4;
var area = 1/2 * (base1 + base2) * height
console.log(area);

// Area of a Parallelogram
var base = 7;
var height = 4;
var area = base * height
console.log(area);


